public class TrainglePattern {
    public static void main(String[] args) {
        int n=6;
        int i,j;
        for (i=1;i<=6;i++)
        {
            for (j=1;j<=i;j++)
            {
                System.out.print(j);
            }
            System.out.println("");
        }
    }
}
